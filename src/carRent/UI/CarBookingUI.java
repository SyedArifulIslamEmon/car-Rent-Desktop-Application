    private void carBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_carBookingButtonActionPerformed
        try {
            CustomerSearchButtonActionPerformed(evt);

            Date fromDate = null;
            Date toDate = null;

            String carRegNo = RegNoComBoBox.getSelectedItem().toString();
            String customerIdString = customerIdTextBox.getText();
            if (customerIdString.equals("")) {
                throw new Exception("Customer Id field Cannot Be Empty");
            }
            int customerId = Integer.parseInt(customerIdString);

            String customerName = customerNameTextBox.getText();

            String customerPhoneNo = customerPhoneNoTextBox.getText();

            String location = TourLocation.getText();

            if (location.equals("")) {
                throw new Exception("Location Cannot Be Empty");
            }

            String fromDateString = fromDateTextBox.getText();

            if (fromDateString.equals("")) {
                throw new Exception("Date Field Cannot Be Empty");
            }
            try {
                fromDate = java.sql.Date.valueOf(fromDateString);
            } catch (Exception e) {
                //JOptionPane.showMessageDialog(rootPane, "Please Enter a Valid Rent Date EX:YYYY-MM-DD");
                throw new Exception("Please Enter a Valid Rent Date EX:YYYY-MM-DD");
            }

            String toDateString = toDateTextBox.getText();

            if (toDateString.equals("")) {
                throw new Exception("Date Field Cannot Be Empty");
            }
            try {
                toDate = java.sql.Date.valueOf(toDateString);
            } catch (Exception e) {
                // JOptionPane.showMessageDialog(rootPane, "Please Enter a Valid Return Date EX:YYYY-MM-DD");
                throw new Exception("Please Enter a Valid Return Date EX:YYYY-MM-DD");
            }
            if (!toDate.after(fromDate)) {
                if (!toDate.equals(fromDate)) {
                    throw new Exception("Invalid Date Range");
                }

            }

            String totalCostString = toalCostTextBox.getText();
            if (totalCostString.equals("")) {
                throw new Exception("Total Cost Cannot Be Empty");
            }
            double totalCost = Double.parseDouble(totalCostString);

            JOptionPane.showMessageDialog(rootPane, aCarBLL.BookCar(carRegNo, customerId, customerName, customerPhoneNo, location, fromDate, toDate, totalCost));
            customerIdTextBox.setText("");
            customerNameTextBox.setText("");
            customerPhoneNoTextBox.setText("");
            TourLocation.setText("");
            fromDateTextBox.setText("");
            toDateTextBox.setText("");
            toalCostTextBox.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }

    }//GEN-LAST:event_carBookingButtonActionPerformed

