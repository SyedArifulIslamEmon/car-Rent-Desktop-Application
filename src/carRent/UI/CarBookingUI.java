    private void CustomerSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustomerSearchButtonActionPerformed
        // TODO add your handling code here:

        try {
            String customerIdString = customerIdTextBox.getText();
            int customerId = Integer.parseInt(customerIdString);
            ArrayList<String> customerInfo = aCarBLL.findCustomer(customerId);

            if (!customerInfo.isEmpty()) {
                customerNameTextBox.setText(customerInfo.get(0));
                customerPhoneNoTextBox.setText(customerInfo.get(1));
            } else {
                JOptionPane.showMessageDialog(rootPane, "Customer not Present In database");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter A Valid CustomerId");
        }

    }//GEN-LAST:event_CustomerSearchButtonActionPerformed

